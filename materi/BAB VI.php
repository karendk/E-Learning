<h3 style="text-align:center"> I’RABNYA ISIM:  RAFA’  DAN NASHAB</h3>
KAIDAH:<br>
	<ol>
<p>	
	<li>Ada perbedaan lain antara susunan  jumlah ismiyyah  dengan susunan  man’ut +  na’at.  Pada  jumlah ismiyyah,  mubtada`  dan  khabar  selalu dibaca  rafa’, sementara  susunan  man’ut  +  na’at,  i’rabnya  tergantung pada status man’utnya.</li> </p>
	<li>I’rab  adalah perubahan akhir kata karena perbedaan  ‘amil-‘amil  yang masuk kepada kata itu.</li> <br>
	<li>Dalam kaidah bahasa Arab,  i’rabnya isim  itu ada tiga,  yaitu rafa’,  nashab  dan jarr.<br>

	Isim,  jika dia berstatus sebagai  mubtada`  atau  khabar  maka dibaca  rafa’. Dalam hal ini perlu dilihat akhir katanya. Apabila  isim  itu  mufrad  (baik  mudzakkar  maupun  mu`annats),  jama’  taksir atau  jama’  mu`annats  salim,  maka,  dalam statusnya sebagai  mubtada`  atau khabar, huruf terakhirnya didhammah (dengan tanwin atau tanpa tanwin). <br>
			Contoh : <br>
		<ol>
			1.	البِءْرُ عَمِيْقٌ <br>
			2.	الرُكْبَةُ كَبِيْرَةٌ<br>
			3.	البُيُوْتُ جَدِيْدَةٌ<br>
			4.	الرِّجَالُ جَمِيْلُوْنَ<br>
			5.	المُدَرِّسَاتُ مُجْتَهِدُوْنَ<br> 
		</ol> <br>
	Apabila  isim  itu  mutsanna  (baik  mudzakkar  maupun  mu`annats) maka tambahan akhir katanya dengan  alif  dan  nun.  Para ahli tata bahasa Arab menyebutkan bahwa tanda rafa’nya adalah alif. <br>
	Contoh : <br>
		<ol>				
			1.	الطَالِبَانِ جَدِيْدَانِ<br>
			2.	الطَالِبَتَانِ جَدِيْدَتَانِ<br>
		</ol><br>
	Sementara apabila  isim  itu berupa  jama’  mudzakkar  salim  maka tambahan akhir katanya adalah waw dan nun. <br>
	Contoh : <ol> المُدَرِّسُوْنَ مُجْتَهِدُوْنَ <br> </ol>
	<p>
	<li> Sementara  isim-isim  itu jika dibaca  nashab  maka huruf terakhir dari  isim mufrad  (baik  mudzakkar  maupun mu`annats) atau  jama’  taksir  itu difathah. <br>
		Contoh:   عَمِيْقًا , كَبِيْرَةً , رِجَالاً;   Huruf terakhir  jama’  mu`annats  salim dikasrah. <br>
		Contoh:  مُجْتَهِدَاتٌ;   Alif  dan  nun  tambahan dari  isim  mutsanna berubah menjadi  ya`  dan  nun,  Contoh: جَدِيْدَتَيْنِ جَدِيْدَيْنِ , ;  dan  waw  dan  nun tambahan  jama’  mudzakkar  salim  berubah menjadi  ya`  dan  nun,  dan huruf sebelum ya` dikasrah. Contoh: مُجْتَهِدِيْنَ
		</li>
