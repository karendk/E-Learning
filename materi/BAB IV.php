<ol><h3 style="text-align-center"> NA’AT  DAN MAN’UT,  ISIM  MA’RIFAH  DAN NAKIRAH,   SERTA PERBANDINGAN ANTARA JUMLAH ISMIYYAH   DAN TARKIB WASHFI  ( MAN’UT  + NA’AT)</h3></ol>
	<ol>
	<li>Jika pada susunan  jumlah ismiyyah  (mubtada`  +  khabar) harus ada kesesuaian dalam hal  jenis dan jumlahnya,  maka ada susunan lain yang hampir sama dengan  jumlah ismiyyah,  yaitu  susunan man’ut + na’at (man’ut = yang disifati, na’at = sifat), hanya saja pada susunan man’ut + na’at ini kesesuaian antara keduanya ditambah dalam hal ma’rifah dan nakirahnya.</li>
	<br>
	<li>Isim  ma’rifah  adalah  isim  yang  maknanya  sudah tertentu.  Sedangkan  isim  nakirah  adalah  isim yang maknanya masih umum. Ada beberapa isim ma’rifah, antara lain adalah nama (orang/kota dll.). <br><br>
	Contoh: مُحَمَّدٌ , مَكَةُ<br><br>
	Isim  nakirah  bisa dijadikan isim  ma’rifah  dengan cara menambahkan kata sandang  alif  dan lam (ال) di depannya. Untuk lebih jelasnya bisa dilihat tabel contoh berikut:<br><br>
