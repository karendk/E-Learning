	<td>Catatan:
	<p>
		Perhatikan cara membuat isim mufrad menjadi mutsanna dan jama’ di bawah ini: </p>
		<ol type ="1">
		<li>Untuk membuat isim mutsanna  yaitu dengan menambahkan  alif  dan nun  di akhir kata, baik mudzakkar maupun mu`annats. 
		<br>
		Contoh: مُدَرِّسٌ menjadi مُدَرِّسَانِ </li>
	<p>
		<li> Untuk membuat  jama’ mudzakkar  yaitu dengan menambahkan  waw  dan  nun  di akhir kata. 
		<br>
		Contoh : مُدَرِّسٌ menjadi مُدَرِّسُوْنَ</li>
	</p>
		<li>Untuk membuat  jama’ mu`annats  yaitu dengan menghilangkan  ta` marbuthah  pada isim mufrad mu`annatsnya lalu menambahkan alif (ا) dan ta` majrurah (ت) di akhir kata. 
		<br>
		Contoh : مُدَرِّسَةٌ menjadi مُدَرِّسَاتٌ </li>
		</td></tr>
</table>
	</ol>
		<ol type="a">
			<li>Jumlah Ismiyyah minimal terdiri dari dua komponen pokok, yaitu mubtada` (subjek) dan khabar (predikat).  Mubtada`  (subjek)  biasanya ada di depan dan  khabar  (predikat)  biasanya berada setelahnya.
		<br>
			Contoh :المُدَرِّسُ مُجْتَهْدٌ (Guru (itu) rajin)
		المُدَرِّسُ adalah mubtada’, sedangkan مُجْتَهْدٌ adalah Khobar.</li>
		<br>	

			<li>Antara mubtada`  dan khabar  harus ada kesesuaian dalam jenis (mudzakkar  atau mu’annatsnya) dan jumlah/adad  (mufrad, mu`annats  atau jama’nya).  Apabila dibuat dalam tabel contoh maka menjadi sebagai berikut:</li>
