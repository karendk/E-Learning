<h3 style="text-align:center"> PEMBAGIAN ISIM, KESESUAIAN DALAM JUMLAH ISMIYYAH, MUBTADA' DAN KHABAR </h3>
KAIDAH : <br>
	<ol>
	<li> a. Dalam bahasa Arab isim dilihat dari jenisnya ada dua, yaitu: </li>
		<ol type="a">
		<li>Mudzakkar (laki-laki). Contoh: مُدَرِّسٌ (guru lk) </li>
	    <li>Mu`annats (perempuan). Contoh: مُدَرِّسَةٌ (guru pr)</li>
	</ol>
	Isim mudzakkar biasanya tidak ada ta` marbuthah ( ة )nya. Sedangkan isim mu`annats biasanya memakai ta` marbuthah ( ة ) <br><br>

	<li>b. Isim dilihat dari jumlahnya ada tiga, yaitu:</li>
		<ol type="a">
			<li>Mufrad (tunggal/satu). Contoh: مُدَرِّسٌ (seorang guru lk)</li>
			<li>Mutsanna (dual/dua). Contoh:   مُدَرِّسَانِ (dua orang guru lk)</li>
			<li>Jama’ (jamak/banyak). Contoh: مُدَرِّسُوْنَ (guru-guru lk)</li>
		</ol>
