	<h3 style="text-align:center"> JAMA' TAKSIR: </h3><br>
	<ol type="1">
		<li>Jama’  yang sudah dijelaskan pada materi PEMBAGIAN ISIM,   KESESUAIAN DALAM JUMLAH ISMIYYAH , MUBTADA`  DAN KHABAR.  Di dalam tata bahasa Arab disebut  jama’ mudzakkar salim  untuk jama’  laki-laki dan  jama’ mu`annats  salim untuk jama’ perempuan. Jadi kata مُدَرِّسُوْنَ  adalah  jama’  mudzakkar  salim dan  kata مُدَرِّسَاتٌ adalah  jama’  mu`annats Salim.</li>
		 	Di dalam bahasa Arab masih ada satu  jama’  lagi, yaitu  jama’ taksir.  Kalau  jama’  mudzakkar salim  akhirnya ditambah huruf  waw  dan  nun,  sementara  jama’ mu`annats salim  akhirnya ditambah huruf  alif  dan  ta`,  maka  jama’ taksir  adalah  jama’  yang berubah dari bentuk mufradnya. Jama’ taksir  disebut juga jamak yang tidak beraturan.  Contoh: رَجُلٌ jama’nya رِجَالٌ ,  كِتاَبٌjama’nya كُتُبٌ Untuk mengetahui jama’ taksirnya suatu kata sebaiknya dihafal.</li>
		<li> Jama’  taksir  ada yang berakal dan ada yang tidak berakal.  Jika tidak berakal hukumnya sama dengan  mu`annats  mufrad,  sedangkan jika berakal,  kalau  mudzakkar  hukumnya sama dengan jama’ mudzakkar  salim  dan kalau  mu`annats  hukumnya sama dengan  jama’ mu`annats salim. Begitu pula halnya dengan  jama’ mu`annats salim,  jika berakal hukumnya  jama’  dan jika tidak berakal hukumnya mu`annats mufrad.</li>
	</ol>
