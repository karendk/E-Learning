<ol><h3 style="text-align:center">JUMLAH ISMIYYAH DIBERI NA’AT</h3></ol>
<ol>
	<li>Kesesuaian antara man’ut + na’at dalam hal ma’rifah dan nakirahnya jangan dipahami pada ada tidaknya kata sandang  ال,  akan tetapi jika  man’ut  ma’rifah,  walaupun tanpa kata sandang  ال, maka na’at harus juga ma’rifah. <br><br>

	Contoh:  مُحَمَّدٌ العَالِمُ(Muhammad yang berilmu itu).<br><br>
	Kata مُحَمَّدٌ adalah isim  ma’rifah, karena dia nama orang,  oleh karena itu disifati oleh na’at isim ma’rifah juga, isim ini menggunakan kata sandang ال, yaitu kata العَالِمُ .</li>

	<li>Susunan  mubtada`  +  khabar  bisa saja keduanya atau salah satunya  disifati dengan  na’at. <br><br>
	Dengan demikian, pola kalimatnya bisa menjadi sebagai berikut: (dibaca dari kiri) <br>

