		KAIDAH:<br>
		<ol>
		<li>Kata dalam bahasa Arab ada tiga yaitu:</li>
			<ol type="a">
				<li>Isim. Contoh: كِتَابٌ  (buku)</li>
		    	<li>Fi’il. Contoh:  كَتَبَ   –   يَكْتُبُ   (menulis)</li>
				<li>Huruf. Contoh:  من – في – عن – على – الى - ب.</li>
			</ol>
			<br>
			Di antara tanda-tanda  isim  yaitu tanwin  di akhir kata,  atau kata sandang alif  dan lam  (  ال) yang ditambahkan di awal kata.
			Di dalam bahasa Indonesia,  isim  biasa diterjemahkan dengan “kata benda”.  Sedang  fi’il  adalah “kata kerja”. Sementara huruf adalah kata tegas yang berfungsi sebagai kata penguhubung.
			Catatan:
			Cara membaca kata sandang alif dan lam ( ال ) jika masuk pada isim ada dua, yaitu:
			<ol type="a">
				<li>Lam yang disukun tetap dibaca sebagaimana adanya, yaitu jika huruf sesudah lam             berupa:  ا , ب ، ج ، ح ، خ ، ع ، غ ، ف ، ق ، ك ، م ، و ، ه ، ي . Alif dan lam ini disebut ال قمرية  (al-qamariyyah). Contoh: الحمد</li>
				<li>Lam tidak dibaca lam akan tetapi bacaannya dilebur seperti huruf sesudahnya, yaitu jika huruf sesudah lam berupa: ت , ث , د , ذ , ر , ز , س , ش , ص , ض , ط , ظ , ل , ن  . Alif dan lam ini disebut  ال شمسية  (al- syamsiyyah). Contoh: الرحيم</li>
			</ol>
				Semua isim yang diberi kata sandang alif dan lam (ال) tidak boleh ditanwin.
				<br><br>
		<li>Di dalam bahasa Arab kalimat (jumlah) ada dua, yaitu:</li>
			<ol type="a">
				<li>Jumlah Ismiyyah. Contoh: الله اكبر (Allah itu maha besar)</li>
				<li>Jumlah fi’liyyah. Contoh:   قرا محمد القران(Muhammad membaca al-Qur’an)</li>
			</ol>
			Untuk mengetahui apakah  jumlah  itu  jumlah ismiyyah  atau  jumlah fi’liyyah  antara lain dengan melihat kata yang pertama pada kalimat itu.  Jika kata pertama pada kalimat itu berupa  isim maka kalimat itu disebut jumlah ismiyyah,  dan jika kata pertama dalam kalimat itu berupa  fi’il maka kalimat itu disebut jumlah fi’liyyah.
		</ol>
